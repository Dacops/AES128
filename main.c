#include <stdio.h>
#include <stdint.h>
#include "aes128.h"

int main(void) {
    uint8_t input[16] = {
        0x32, 0x88, 0x31, 0xe0,
        0x43, 0x5a, 0x31, 0x37,
        0xf6, 0x30, 0x98, 0x07,
        0xa8, 0x8d, 0xa2, 0x34
    };

    uint8_t key[16] = {
        0x2b, 0x28, 0xab, 0x09,
        0x7e, 0xae, 0xf7, 0xcf,
        0x15, 0xd2, 0x15, 0x4f,
        0x16, 0xa6, 0x88, 0x3c
    };

    aes128_encrypt(input, key);

    printf("Encrypted AES:\n");
    for (int i = 0; i < 16; ++i) {
        printf("%02x ", input[i]);
        if ((i + 1) % 4 == 0)
            printf("\n");
    }

    return 0;
}
